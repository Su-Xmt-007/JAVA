
class Operator {

    public static void main(String args[]) {

        int a = 3;
        int b = 6;
        System.out.println(a == b); //Equal to
        System.out.println(a != b); // Not Equal to

        int c = 6, d = 6;
        System.out.println(c == d);
        System.out.println(c != d);

        System.out.println(c > d); // Greater than
        System.out.println(c < d); // Less than

        System.out.println(c >= d); // Greater Than equal to
        System.out.println(c <= d); // Less than equal to

    }
}
